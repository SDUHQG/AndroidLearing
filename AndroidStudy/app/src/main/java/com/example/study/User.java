package com.example.study;

public class User {
    String user_id;
    String user_name;
    boolean user_sex;
    int user_age;
    String user_password;
    String user_pic;
    String user_campus;
    String user_phone;
    float user_balance;

    public String getUser_id() {
        return user_id;
    }

    public void setUser_id(String user_id) {
        this.user_id = user_id;
    }

    public String getUser_name() {
        return user_name;
    }

    public void setUser_name(String user_name) {
        this.user_name = user_name;
    }

    public boolean isUser_sex() {
        return user_sex;
    }

    public void setUser_sex(boolean user_sex) {
        this.user_sex = user_sex;
    }

    public int getUser_age() {
        return user_age;
    }

    public void setUser_age(int user_age) {
        this.user_age = user_age;
    }

    public String getUser_password() {
        return user_password;
    }

    public void setUser_password(String user_password) {
        this.user_password = user_password;
    }

    public String getUser_pic() {
        return user_pic;
    }

    public void setUser_pic(String user_pic) {
        this.user_pic = user_pic;
    }

    public String getUser_campus() {
        return user_campus;
    }

    public void setUser_campus(String user_campus) {
        this.user_campus = user_campus;
    }

    public String getUser_phone() {
        return user_phone;
    }

    public void setUser_phone(String user_phone) {
        this.user_phone = user_phone;
    }

    public float getUser_balance() {
        return user_balance;
    }

    public void setUser_balance(float user_balance) {
        this.user_balance = user_balance;
    }
}
